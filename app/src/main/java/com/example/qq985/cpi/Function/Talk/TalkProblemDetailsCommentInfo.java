public class TalkProblemDetailsCommentInfo {

    String problemName;
    String problemCommenter;
    String problemCommentDate;
    String problemCommentContent;

    public String getProblemName() {
        return problemName;
    }

    public void setProblemName(String problemName) {
        this.problemName = problemName;
    }

    public String getProblemCommenter() {
        return problemCommenter;
    }

    public void setProblemCommenter(String problemCommenter) {
        this.problemCommenter = problemCommenter;
    }

    public String getProblemCommentDate() {
        return problemCommentDate;
    }

    public void setProblemCommentDate(String problemCommentDate) {
        this.problemCommentDate = problemCommentDate;
    }

    public String getProblemCommentContent() {
        return problemCommentContent;
    }

    public void setProblemCommentContent(String problemCommentContent) {
        this.problemCommentContent = problemCommentContent;
    }
}
